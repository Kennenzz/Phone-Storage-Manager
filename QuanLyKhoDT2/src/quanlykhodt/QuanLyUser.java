    public final void showUser(){
        tableModel.setRowCount(0);
        Userlist = UserModify.findAll();
        Userlist.forEach((User a) -> {
            tableModel.addRow(new Object[]{a.getUserName(), a.getPassWord(), a.getVaiTro(), a.getEmail()});
        });
}
