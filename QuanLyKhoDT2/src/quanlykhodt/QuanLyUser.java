    private void ThembtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ThembtnActionPerformed
        // TODO add your handling code here:
        String user = Usertxt.getText();
        String pass = Passtxt.getText();
        String vaitro = (String) VaiTrocbb.getSelectedItem();
        String email = Emailtxt.getText();
        User a = new User(user, pass, email, vaitro);
        UserModify.Insert(a);
        showUser();
    }//GEN-LAST:event_ThembtnActionPerformed

    private void XoabtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_XoabtnActionPerformed
        // TODO add your handling code here:
        if(selectedP >= 0){
            User a = Userlist.get(selectedP);
            UserModify.delete(a.getUserName());
            showUser();
        }
    }//GEN-LAST:event_XoabtnActionPerformed

    private void SuabtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SuabtnActionPerformed
        // TODO add your handling code here:
        if(selectedP >= 0){
            User a = new User();
            a.setEmail(Emailtxt.getText());
            a.setUserName(Usertxt.getText());
            a.setVaiTro((String) VaiTrocbb.getSelectedItem());
            a.setPassWord(Passtxt.getText());
            UserModify.update(a);
            showUser();
        }
    }//GEN-LAST:event_SuabtnActionPerformed

    private void UsertblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_UsertblMouseClicked
        // TODO add your handling code here:
        if(selectedP >= 0){
            User a = Userlist.get(selectedP);
            Usertxt.setText(a.getUserName());
            Passtxt.setText(a.getPassWord());
            VaiTrocbb.setSelectedItem(a.getVaiTro());
            Emailtxt.setText(a.getEmail());
        }
    }//GEN-LAST:event_UsertblMouseClicked

