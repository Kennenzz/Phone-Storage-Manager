    public final void showDienThoai(){
        tableModel.setRowCount(0);
        DienThoailist = DienThoaimodify.findAll();
        DienThoailist.forEach(dienthoai -> {
            tableModel.addRow(new Object[]{dienthoai.getMaDT(), dienthoai.getTenDT(), dienthoai.getHDH(), dienthoai.getMauDT(), dienthoai.getSoLuong(), dienthoai.getNgaynhap()} );
        });
    }
