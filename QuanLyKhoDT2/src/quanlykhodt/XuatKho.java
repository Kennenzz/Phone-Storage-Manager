    private void XuatbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_XuatbtnActionPerformed
        // TODO add your handling code here:
            DienThoai dtc = DienThoailist.get(selectedP);
            int xuat = Integer.parseInt(JOptionPane.showInputDialog(rootPane, "Nhập số lượng xuất"));
            if(xuat > dtc.getSoLuong()){
                JOptionPane.showMessageDialog(rootPane, "Số lượng xuất vượt quá số lượng tồn kho");
            }
            else{
                Phieu a = new Phieu(dtc.getMaDT(), dtc.getTenDT(), dtc.getHDH(), dtc.getMauDT(), xuat, dtc.getNgaynhap());
                dtc.setSoLuong(dtc.getSoLuong() - xuat);
                DienThoaimodify.update(dtc);
                XuatPhieu(a);
                showDienThoai();
            }
    }//GEN-LAST:event_XuatbtnActionPerformed

    public void XuatPhieu(Phieu a){
        long millis=System.currentTimeMillis();   
        java.sql.Date date=new java.sql.Date(millis);  
        PreparedStatement statement = null;
        Connection conn = null;
        try{
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/dienthoai","root", "");
            String sql = "INSERT INTO phieuxuat (MaDT, TenDT, HDH, MauDT, SoLuong, Ngay) values (?, ?, ?, ?, ?, ?)";
            statement = conn.prepareCall(sql);
             statement.setString(1, a.getMaDT());
             statement.setString(2, a.getTenDT());
             statement.setString(3, a.getHDH());
             statement.setString(4, a.getMauDT());
             statement.setInt(5,a.getSoluong());
            statement.setDate(6, date);
             statement.execute();
        } catch (SQLException ex) {
            Logger.getLogger(NhapKho.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
